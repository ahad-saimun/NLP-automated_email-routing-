# Library and Data Load
import torch
from peft import LoraConfig, get_peft_model, TaskType
from transformers import AutoModelForCausalLM, AutoTokenizer, TrainingArguments, Trainer, DataCollatorForLanguageModeling
from datasets import Dataset
from datapreparation import load_and_prepare_data

# 1. Data load
train_ds, val_ds, test_ds, label2id, id2label = load_and_prepare_data()

# 2. GPT2 Model and Tokenizer setup
model_name = "gpt2"
tokenizer = AutoTokenizer.from_pretrained(model_name)
tokenizer.pad_token = tokenizer.eos_token

# Data Preprocessing
def tokenize_function(examples):

    texts = [f"Subject: {s}\nBody: {b}\nDepartment: {q}"
             for s, b, q in zip(examples['subject'], examples['body'], examples['queue'])]
    return tokenizer(texts, truncation=True, padding="max_length", max_length=128)

train_dataset = Dataset.from_pandas(train_ds).map(tokenize_function, batched=True)
val_dataset = Dataset.from_pandas(val_ds).map(tokenize_function, batched=True)

# LoRA configuration
lora_config = LoraConfig(
    r=16,
    lora_alpha=32,
    target_modules=["c_attn"],
    lora_dropout=0.05,
    task_type=TaskType.CAUSAL_LM
)


base_model = AutoModelForCausalLM.from_pretrained(model_name)
lora_model = get_peft_model(base_model, lora_config)

# model train
training_args = TrainingArguments(
    output_dir="./gpt2-full-lora-router",
    per_device_train_batch_size=4,
    eval_strategy="epoch",
    learning_rate=2e-4,
    num_train_epochs=1,
    save_strategy="epoch",
    logging_steps=10,
    fp16=True if torch.cuda.is_available() else False,
    report_to="none"
)

trainer = Trainer(
    model=lora_model,
    args=training_args,
    train_dataset=train_dataset,
    eval_dataset=val_dataset,
    data_collator=DataCollatorForLanguageModeling(tokenizer=tokenizer, mlm=False)
)

print("\n--- GPT-2 Full Fine-tuning is starting... ---")
trainer.train()

"""Accuracy test"""

def get_gpt2_lora_prediction(subject, body):
    prompt = f"Subject: {subject}\nBody: {body}\nDepartment:"
    inputs = tokenizer(prompt, return_tensors="pt").to(lora_model.device)

    with torch.no_grad():
        output_tokens = lora_model.generate(**inputs, max_new_tokens=5, pad_token_id=tokenizer.eos_token_id)

    response = tokenizer.decode(output_tokens[0], skip_special_tokens=True)
    return response.split("Department:")[-1].strip()



# data load
import torch
from transformers import AutoTokenizer, AutoModelForSequenceClassification, TrainingArguments, Trainer
from datasets import Dataset
from datapreparation import load_and_prepare_data
import numpy as np
import evaluate

train_ds, val_ds, test_ds, label2id, id2label = load_and_prepare_data()

model_name = "distilbert-base-uncased"
tokenizer = AutoTokenizer.from_pretrained(model_name)

# data processing
def preprocess_function(examples):
    inputs = [f"{s} [SEP] {b}" for s, b in zip(examples['subject'], examples['body'])]
    tokenized = tokenizer(inputs, truncation=True, padding="max_length", max_length=128)
    tokenized["labels"] = [label2id[label] for label in examples["queue"]]
    return tokenized

train_dataset = Dataset.from_pandas(train_ds).map(preprocess_function, batched=True)
val_dataset = Dataset.from_pandas(val_ds).map(preprocess_function, batched=True)

# model and accuracy metric setup
model = AutoModelForSequenceClassification.from_pretrained(
    model_name, num_labels=len(label2id), id2label=id2label, label2id=label2id
)

metric = evaluate.load("accuracy")

def compute_metrics(eval_pred):
    logits, labels = eval_pred
    predictions = np.argmax(logits, axis=-1)
    return metric.compute(predictions=predictions, references=labels)

# model train
training_args = TrainingArguments(
    output_dir="./distilbert-plus-80-accuracy",
    learning_rate=3e-5,
    per_device_train_batch_size=16,
    num_train_epochs=3,
    weight_decay=0.01,
    eval_strategy="epoch",
    save_strategy="epoch",
    load_best_model_at_end=True,
    fp16=True if torch.cuda.is_available() else False,
    report_to="none"
)

trainer = Trainer(
    model=model,
    args=training_args,
    train_dataset=train_dataset,
    eval_dataset=val_dataset,
    tokenizer=tokenizer,
    compute_metrics=compute_metrics,
)

print("\n--- Training DistilBERT+ Classifier for 80% Accuracy ---")
trainer.train()

# accuracy test
test_dataset = Dataset.from_pandas(test_ds).map(preprocess_function, batched=True)
results = trainer.evaluate(test_dataset)

print(f"\nDistilBERT+ Classifier Final Accuracy: {results['eval_accuracy']*100:.2f}%")


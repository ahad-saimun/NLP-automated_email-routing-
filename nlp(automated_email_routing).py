pipe = pipeline("text-generation", model="distilgpt2", device=0 if torch.cuda.is_available() else -1)

# 3. function prediction (Few-Shot Prompting)
def get_gpt2_prediction_improved(subject, body):

    few_shot_examples = """
    Task: Classify emails into: Technical Support, Customer Service, Billing and Payments, Sales and Pre-Sales, General Inquiry.

    Subject: Refund request for order #123
    Body: I want a refund for my last purchase as the product is damaged.
    Department: Customer Service

    Subject: Cannot reset my password
    Body: The reset link is not working on the login page.
    Department: Technical Support

    Subject: Quote for bulk order
    Body: We are interested in buying 50 units. What is the discount?
    Department: Sales and Pre-Sales
    """

    prompt = f"""{few_shot_examples}
    Subject: {subject}
    Body: {body}
    Department:"""

    # do_sample=False and max_new_tokens reduce  3-5 so that do not come wrong result
    output = pipe(prompt, max_new_tokens=5, num_return_sequences=1, do_sample=False, pad_token_id=50256)

    response = output[0]['generated_text'].split("Department:")[-1].strip()
    return response

def evaluate_method_1():
    correct = 0
    total = len(test_ds)
    print(f"Testing Method 1 on {total} samples with Few-Shot Prompting...")

    for i in tqdm(range(total)):
        actual = test_ds.iloc[i]['queue']
        subject = test_ds.iloc[i]['subject']
        body = test_ds.iloc[i]['body']

        predicted = get_gpt2_prediction_improved(subject, body)

        # predictiction in between actual level (Case insensitive)
        if actual.lower() in predicted.lower():
            correct += 1

    accuracy = (correct / total) * 100
    return accuracy

# run and show result
